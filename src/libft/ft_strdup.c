#include "libft.h"

char	*ft_strdup(char const *s)
{
	int		i;
	int		len;
	char	*str;

	len = 0;
	while (s[len] != '\0')
		len++;
	str = malloc(len + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
